namespace ArcChat.UI.Theme;

/// <summary>
/// Theme variants exposed by the NC03 shell from NextChat app/store/config.ts theme values.
/// </summary>
public enum ColorScheme
{
    /// <summary>
    /// Follow the operating system theme preference.
    /// </summary>
    System,

    /// <summary>
    /// Force NextChat light theme colors.
    /// </summary>
    Light,

    /// <summary>
    /// Force NextChat dark theme colors.
    /// </summary>
    Dark,
}
